SELECT *
FROM PUBLIC.ALARM
WHERE ('01/03/2022' >= START_DATE
							AND '01/03/2022' <= END_DATE)
	OR ('01/05/2022' >= START_DATE
					AND '01/05/2022' <= END_DATE)
	OR ('01/03/2022' < START_DATE
					AND '01/05/2022' > END_DATE)