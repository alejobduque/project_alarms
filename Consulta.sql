SELECT *
FROM PUBLIC."alarm"
WHERE ('01/03/2022' BETWEEN START_DATE AND END_DATE)
	OR ('01/05/2022' BETWEEN START_DATE AND END_DATE)
	OR ('01/03/2022' < START_DATE
					AND '01/05/2022' > END_DATE)
					
